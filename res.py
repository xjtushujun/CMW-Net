def ResNet34(num, pretrained=False):   
    if pretrained:
        model = ResNet(block=BasicBlock, layers=[3, 4, 6, 3])
        state_dict = torch.utils.model_zoo.load_url(model_urls['resnet34'], progress=True)
        model.load_state_dict(state_dict)
        model.fc = MetaLinear(512, num)
    else:
        model = ResNet(block=BasicBlock, layers=[3, 4, 6, 3], num_classes=num)

    
    return model
    


